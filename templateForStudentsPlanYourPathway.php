  <h3 style="padding-top: 52px; border-top: 1px solid #B7B7B7;"><img class="headline" src="<?php bloginfo('template_directory'); ?>/style/images/planYourPathwayStepsHeader.png" alt="What tests you need to take to get into college and where to get help" /></h3>
            <p>Many colleges require you to submit scores from a college admissions test when you apply. There are two college admissions test, the <a href="http://sat.collegeboard.com/home">SAT</a> and the <a href=http://www.ACTstudent.org>ACT</a>. In Philadelphia, more students take the SAT than the ACT, but you may want to take both tests. They have a different format and some people get better scores on one or the other. The SAT tests math, reading, and writing and is broken down into many sections that are around twenty minutes long. The ACT tests math, reading, writing, and science and is broken down into four longer sections. Both tests are about four hours long.
            <p>
              In school in 11th grade you will take the <a href="http://www.collegeboard.com/testing/">PSAT</a>, which is the practice test for the SAT. You should take the SAT or ACT for the first time in the spring of your junior year in high school. You can retake them again in your senior year of high school if your scores are not as good as you want them to be.         
              <p>
             All of these tests cost money to take, but fee waivers are available through many high schools and sometimes through the testing companies. Talk to your school counselor for more information on fee waivers. You may also want to check the websites of the SAT and ACT for more information on fees and waivers.</p>
              You have to register for the SAT and ACT at least a month before taking the test. 
              <p>
 <a href="http://sat.collegeboard.com/register" target_blank>Click here for information on registering for the SAT, </a> or 
                <a href="http://www.actstudent.org/regist/index.html" target_blank>here for information on registering for the ACT. </a>
              <p>
              <p style="margin-bottom: 12px;">  The SAT and ACT are challenging, but there are a lot of resources that can help you prepare for them. The websites for both tests offer free online study tools, like practice questions. Many <a href="http://libwww.freelibrary.org/branches/brnlist.cfm" target_blank>libraries</a> carry copies of SAT and ACT prep books with detailed information, tips, and full length practice tests. 
  <p>
    Many organizations in the city offer free SAT and ACT prep classes. You can search our <a href="http://www.phillygoes2college.org/category/events/month" target_blank>calendar</a> to find a class that works for you.

    <p> For low cost individualized help you can contact <a href="http://www.jevshumanservices.org/programs/college-strategies.html">JEVS</a>, they offer a sliding scale fee for classes and tutoring services.
              
    
