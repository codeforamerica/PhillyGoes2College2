  <p>There are some tests your student needs to prepare for before applying to college. There is the <a href="http://www.ACTstudent.org">ACT</a> which is a national college admissions examination that consists of subject area tests in English, Math, Reading and Science. Then there&#8217;s the <a href="http://www.collegeboard.com/testing/">SAT</a> which is also a national college admissions exam and the prep test (<a href="http://www.collegeboard.com/testing/">PSAT</a>). There is usually no charge to take the PSAT which is often given to students before their senior year in high school, but when you take the ACT or SAT there is often a fee you have to pay to take it and the tests are only offered during certain times of the year. Check out their websites for more information.</p>
  <p style="margin-bottom: 12px;">Fortunately, there are a good number of programs that can help you prepare for these tests and some of prep classes are offered for free or low cost. Check them out on these websites:</p>
